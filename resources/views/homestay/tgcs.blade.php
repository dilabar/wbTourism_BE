                <p class="text">The Tourist Guides play a significant role in promoting the destinations and products
                    of the State and providing an enriching experience.</p>
                <h3 class="headingrtsp">BACKGROUND</h3>
                <p class="text">The competence of the person directly interacting with the visitor would have a major
                    role to play. This role would invariably be played by the Tourist Guides who would be directly
                    interacting with the visitor. Direct interaction with the tourists makes them even more responsible
                    for projecting the state in a correct manner by furnishing factually correct information about the
                    destination, ensuring the safety of the tourists as well as providing a pleasant and satisfying
                    experience for them during their visits.<br><br>
                    The Government of West Bengal, Department of Tourism, hereby, notifies the West Bengal Tourist Guide
                    Certification Scheme for the selection, training and Certification of the West Bengal Tourist
                    Guides. This Scheme would become operational from the date of notification.</p>
                <h3 class="heading5">1. CATEGORIES OF TOURIST GUIDES : </h3>
                <p class="text"><strong style="font-style:italic;">There shall be two categories of Tourist
                        Guides:</strong></p>
                <div class="bullet">
                    <ul>
                        <li><strong>New Tourist Guide:</strong> These will be the fresh intending applicants fulfilling
                            the prescribed eligibility criteria as laid down in para 5a) who will be registered &amp;
                            initially given the Basic training on various aspects of tourism and orientation about
                            essential communication and presentation skills, duration of which will be 04 weeks (192
                            hours).</li>
                        <li><strong>Veteran (Existing) Tourist Guide:</strong> These are the Existing Tourist Guides who
                            have been working in the profession as a Guide for at least three years in various tourism
                            places. They will be given an opportunity of getting recognized provided they fulfill the
                            eligibility criteria mentioned in para 5b). They will be provided reskilling in the course
                            for 'Recognition of Prior Learning', duration of which will be 02 weeks (96 hours).</li>
                    </ul>
                </div>
                <h3 class="heading5">2. ELIGIBILITY &amp; REGISTRATION PROCEDURE:</h3>

                <div class="lower_alfa">
                    <ul>
                        <li><strong>Eligibility criteria for New Tourist Guide:</strong>
                            <ul>
                                <li>The candidate must have attained the age of 18 years as on the date of application.
                                </li>
                                <li>The candidate should have completed 10th Standard or equivalent examination
                                    conducted by any recognized Board by State Government/Government of India on or
                                    before the date of registration to the course.</li>
                                <li>Should be enthusiastic about history, culture and tourism</li>
                            </ul>
                        </li>
                        <li><strong>Eligibility criteria for Veteran (Existing) guides to register for the Recognition
                                of Prior Learning Course.</strong>
                            <ul>
                                <li>The candidate must have attained the age of 18 years as on the date of application.
                                </li>
                                <li>The candidate should have completed 8 standard examination.</li>
                                <li>Should be enthusiastic about history, culture and tourism</li>
                                <li>Should have been working in the profession as a Guide for at least 03 years and
                                    would have to upload any Veteran (Existing) valid certificate of IITF and IITG
                                    Certification Programme OR a certificate of experience in the said profession duly
                                    certified by competent local authority (BDO in case of Block/SDO for municipal
                                    areas/Director Tourism for Kolkata) based on local enquiry/recommendation of Gram
                                    Panchayat/Municipality.</li>
                            </ul>
                        </li>
                    </ul>
                </div>
                <h3 class="heading5">3. DOCUMENTS TO BE UPLOADED/SUBMITTED.</h3>
                <p class="text">Intending applicants both New/Veteran (Existing) guides may register themselves online
                    in the website of West Bengal Tourism Department and upload all the following documents OR submit
                    the application form &amp; documents at Office of the District Magistrate/BDO/Office of Director
                    Tourism for Kolkata Municipal Corporation : -</p>

                <div class="bullet">
                    <ul>
                        <li>Document showing age proof (copy of 10th Class Board Certificate/birth certificate issued by
                            the
                            competent authority/EPIC/Aadhaar Card)</li>
                        <li>Educational Qualification -Standard 10 or equivalent certificate wherever applicable,
                            (issued by any
                            recognized Board by Government of India/State Government)/Standard 8 result as applicable,
                            and</li>
                        <li>Recognition of Prior Learning Course-The Veteran (Existing) Guide must submit valid
                            certificate of
                            IITF and IITG Certification Programme OR a certificate of experience in the said profession
                            duly
                            certified by competent local authority (BDO in case of Block/SDO for municipal
                            areas/Director Tourism for Kolkata), based on local enquiry/recommendation of Gram
                            Panchayat/Municipality - applicable only for Veteran (Existing) Guide applicants.</li>

                    </ul>
                </div>
                <p class="text">The Registered Tourist Guides will be authorized to work in West Bengal on a full time
                    basis independently or can be engaged by the stakeholders of the tourism sector. It is mandatory for
                    the Tourism Service Providers recognized by the Department of Tourism to engage only the certified
                    Tourist Guides.<br><br>
                    The Training &amp; Resource Partner selected under the Utkarsh Bangla module would be entrusted with
                    the
                    responsibility of preparation &amp; upgradation of the entire training module for the course,
                    conduct of examination, training &amp; facilitating internship, documentation, certification process
                    and other facilitation process in consultation with the Tourism Department.<br><br>

                    The Department reserves the right to suitably amend &amp; modify the provisions in this scheme as
                    suited to the evolving changes in the Tourism and Hospitality Industry.
                </p>
