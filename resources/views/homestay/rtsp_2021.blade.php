                <p class="content_text">The tourism service providers play a significant role in promoting the destinations
                    and products of the State and providing an enriching experience. The Guidelines is being framed with
                    the objective to recognize and provide support to the eligible Tour Operators, Travel Agents and
                    Tourist Transport Operators promoting West Bengal. The aim would be to provide skill development and
                    marketing support to the eligible stakeholders so as to ensure appropriate service standards across
                    the State.</p>
                <h3 class="headingrtsp">1. BACKGROUND</h3>
                <p class="content_text">The Government of West Bengal, Department of Tourism had been giving recognition to
                    tourism stake holders in the past for which Rules for Voluntary Recognition of Tour and Travel
                    Operators was published in April 2006. With the emerging changing trends in the Tourism Sector, it
                    was felt appropriate to provide proper classification of the various Tourism Stakeholders under
                    various categories such as Inbound Tour Operators, Domestic Tour Operators, MICE Tour Operators,
                    Cruise Tour Operators, Adventure Tour Operators, Travel Agents and Tourist Transport Operators and
                    provide Recognition accordingly.<br><br>
                    Further, the Corona virus (COVID-19) pandemic has triggered an unprecedented crisis in the tourism
                    sector. All these factors have necessitated that a comprehensive Guidelines for giving Recognition
                    to Tourism Service Providers are introduced appropriately.</p>
                <p class="padding"><strong>Definition</strong></p>

                <div class="roman">
                    <ul>
                        <li><strong>Tour Operators</strong> <br>A Tour Operator is one who plans, promotes and operates
                            tours and makes arrangements for transport, accommodation, sightseeing, entertainment and
                            other tourism related services and activities for tourists visiting destinations within the
                            state of West Bengal. Tour Operators would include:
                            <ul>
                                <li><strong>Inbound Tour Operator:</strong> Tour Operators Specialized/having expertise
                                    in handling/operating foreign Tourists and promoting West Bengal as a primary
                                    destination in foreign source markets.</li>
                                <li><strong>Domestic Tour Operator. (Inter &amp; Intra State):</strong> Tour Operators
                                    Specialized/having expertise in handling/operating domestic Tourists and promoting
                                    West Bengal as a primary destination in local and national source markets.</li>
                                <li><strong>MICE Tour Operator:</strong> Tour Operators making arrangements for
                                    Meetings, Incentives, Conferences and Exhibitions (MICE) held within West Bengal.
                                </li>
                                <li><strong>Cruise Tour Operator:</strong> Tour Operators Specialized/having expertise
                                    in handling/operating river cruises with accommodation facilities within the state
                                    of West Bengal and exclusively promotes Cruise Tourism in domestic &amp; foreign
                                    source markets.</li>
                                <li><strong>Adventure Tour Operator:</strong> Tour Operators engaged in activities
                                    related to Adventure Tourism in West Bengal such as mountaineering, rock climbing,
                                    trekking, safaris, bicycling, motorcycling, motoring tours and making required
                                    arrangements for the same.</li>
                            </ul>
                        </li>
                        <li><strong>Travel Agents</strong><br> A Travel Agent is one who provides travel services and
                            makes arrangements of travel related services such as tickets for travel by air, rail, ship,
                            passport, visa, etc. for the tourists visiting West Bengal, including through the online
                            mode. They may also arrange accommodation, tours, entertainment &amp; other tourism related
                            services directly or through tour operators.</li>
                        <li><strong>Tourist Transport Operator-TTO</strong><br> Tourist Transport Operator is one, who
                            provides tourist transport like cars, coaches, boats etc. to tourists for transfers,
                            sightseeing and journeys to tourist places, etc. within West Bengal, including through the
                            online mode.</li>
                        <li>"Competent Authority" to grant Recognition of Tour Operators / Travel Agents &amp; Tourist
                            Transport Operators means the Director of Tourism, Government of West Bengal.</li>
                    </ul>
                </div>
                <h3 class="heading">2. APPLICABILITY</h3>
                <p class="content_text">This Guidelines is applicable to tourism service providers engaged in the profession/
                    business/activities of providing services to the tourists, within the State of West Bengal and
                    include the following:-</p>
                <div class="bullet">
                    <ul>
                        <li>Tour Operators (Inbound, Domestic, MICE, Cruise &amp; Adventure)</li>
                        <li>Travel Agents &amp; Tourist Transport Operators</li>
                    </ul>
                </div>
                <h3 class="heading">3. CATEGORIES</h3>
                <p class="content_text">Recognition may be given to Tourism Service Providers (in the segments of Tour
                    Operators/ Travel Agents/ Tourist Transport Operators) under the following categories:</p>
                <div class="roman">
                    <ul>
                        <li><strong>Green Shoots / Start Ups (GSSP):</strong> A new organization registered with RoC
                            (OMC / LLP / Private Limited / Public Limited) which has just ventured into the tourism
                            sector and has no previous experience in the field of tourism.</li>
                        <li><strong>Experienced Tourism Service Providers (ETSP):</strong> Those with minimum 03 years
                            experience in the tourism
                            sector.
                        </li>
                    </ul>
                </div>

